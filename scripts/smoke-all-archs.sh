#!/bin/bash
# smoke-all-archs.sh — comprehensive coherence smoke across every Swift
# model architecture in `Libraries/MLXLLM/Models/` and `Libraries/MLXVLM/Models/`.
#
# One representative model per architecture (4-bit when available; bf16 fallback
# for arches without a public 4-bit MLX checkpoint). Each model is exercised
# under `--kv none`, `--kv affine4`, and `--kv turbo4` to verify the three
# canonical KV-compression paths produce coherent output end-to-end.
#
# Run overnight on first invocation — uncached models will trigger HF
# downloads (~125GB total across ~25 new architectures). Subsequent runs hit
# the local cache and complete in ~60-90 minutes wall time on M1 Max 64GB.
#
# Output:
#   benchmarks/smoke-all-archs-YYYY-MM-DD.log   (full bench output)
#   benchmarks/smoke-all-archs-YYYY-MM-DD.md    (pass/fail matrix + first ~120
#                                                chars of generated output for
#                                                coherence verification)
#
# Usage:
#   ./scripts/smoke-all-archs.sh           # full sweep (LLMs + VLMs)
#   ./scripts/smoke-all-archs.sh llms      # LLMs only
#   ./scripts/smoke-all-archs.sh vlms      # VLMs only
#
# Override the KV configs (default: none,affine4,turbo4):
#   SMOKE_KV=none ./scripts/smoke-all-archs.sh

set -uo pipefail

cd "$(dirname "$0")/.."

KV="${SMOKE_KV:-none,affine4,turbo4}"
DATE="$(date +%Y-%m-%d)"
LOG_LLM="benchmarks/smoke-all-archs-${DATE}.llm.log"
LOG_VLM="benchmarks/smoke-all-archs-${DATE}.vlm.log"
SUMMARY="benchmarks/smoke-all-archs-${DATE}.md"
mode="${1:-all}"

# ─── Architecture coverage ────────────────────────────────────────────────────
# One model per `Libraries/MLXLLM/Models/*.swift` and `Libraries/MLXVLM/Models/*.swift`
# architecture. Sizes biased to small/medium when available so the sweep stays
# under ~90 min on warm cache. Skipped: Bitnet (1.58-bit), DeepseekV3/V4 (>>64GB),
# MiniMax/MiniMaxM2 (456B+), Lille130m + NanoChat (toy), Gemma 1.x + Phi 1.x
# (superseded), AfMoE + Olmo3 (HF gated), BailingMoe (no 4bit, only 2bit DWQ).

