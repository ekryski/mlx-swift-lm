# ─── Run ─────────────────────────────────────────────────────────────────────

run_llms() {
    local llm_count=$(echo "$LLMS" | tr ',' '\n' | grep -c .)
    echo "[SMOKE] LLM sweep — $llm_count architectures × $(echo "$KV" | tr ',' '\n' | grep -c .) KV configs"
    echo "[SMOKE] Logging to: $LOG_LLM"
    ./scripts/benchmark.sh --model "$LLMS" --method simple --kv "$KV" 2>&1 | tee "$LOG_LLM"
}

run_vlms() {
    local vlm_count=$(echo "$VLMS" | tr ',' '\n' | grep -c .)
    echo "[SMOKE] VLM sweep — $vlm_count architectures × $(echo "$KV" | tr ',' '\n' | grep -c .) KV configs"
    echo "[SMOKE] Logging to: $LOG_VLM"
    ./scripts/benchmark.sh --model "$VLMS" --method vision --kv "$KV" 2>&1 | tee "$LOG_VLM"
}

# Markdown summary builder — pass/fail per cell + first chars of generated output.
build_summary() {
    {
        echo "# Architecture smoke matrix — $DATE"
        echo ""
        echo "Generated by \`scripts/smoke-all-archs.sh\` — one model per Swift"
        echo "architecture, exercised under each KV-compression scheme."
        echo ""
        for log in "$LOG_LLM" "$LOG_VLM"; do
            [ -f "$log" ] || continue
            echo ""
            if [[ "$log" == *.llm.log ]]; then
                echo "## LLM smoke (\`--method simple\`)"
            else
                echo "## VLM smoke (\`--method vision\`)"
            fi
            echo ""
            echo "| Model | KV | Result | Tok/s | First chars of output |"
            echo "|---|---|---|---|---|"
            # Re-parse bench log and emit one row per (model, kv).
            awk '
                /^\[INFO\] \[[0-9]+\/[0-9]+\] model=/ {
                    match($0, /model=([^ ]+) method=([^ ]+) quant=([^ ]+) kv=([^ ]+)/, arr)
                    cur_model = arr[1]; cur_kv = arr[4]
                    cur_output = ""; cur_tok = ""
                    cur_pass = 0
                }
                /^\[BENCH\] Output:/ {
                    sub(/^\[BENCH\] Output: /, ""); cur_output = substr($0, 1, 110)
                }
                /^\[BENCH\] Generation:/ {
                    sub(/^\[BENCH\] Generation: /, ""); cur_tok = $1 " " $2
                }
                /Test run with 1 test in 1 suite passed/ {
                    cur_pass = 1
                    gsub(/\|/, "\\|", cur_output)
                    print "| `" cur_model "` | " cur_kv " | ✅ PASS | " cur_tok " | " cur_output " |"
                }
                /^\[ERROR\] Run failed/ {
                    print "| `" cur_model "` | " cur_kv " | ❌ FAIL | — | (see log) |"
                }
            ' "$log"
        done
    } > "$SUMMARY"
    echo ""
    echo "[SMOKE] Summary written to: $SUMMARY"
}

case "$mode" in
    llms) run_llms ;;
    vlms) run_vlms ;;
    all)  run_llms; run_vlms ;;
    *)
        echo "Usage: $0 [all|llms|vlms]" >&2
        exit 2
        ;;
esac

build_summary
